package com.liubin.basic;


public class SwitchStr {

    public static void main(String[] args) {
        String str1 = "1";
        switch (str1) {
            case "1":
                System.out.println("1");
                break;
            case "2":
                System.out.println("2");
                break;
            default:
                System.out.println("3");
        }
    }
}
